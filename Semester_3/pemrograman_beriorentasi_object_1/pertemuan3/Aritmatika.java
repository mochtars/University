public class Aritmatika {
  public static void main(String[] args) {
    System.out.printf("15 + 9 = %.2f\n", (15 + 9.0));
    System.out.printf("15 - 9 = %.2f\n", (15 - 9.0));
    System.out.printf("15 * 9 = %.2f\n", (15 * 9.0));
    System.out.printf("15 / 9 = %.2f\n", (15 / 9.0));
    System.out.printf("15 %% 9 = %.2f\n", (15 % 9.0));
  }
}
